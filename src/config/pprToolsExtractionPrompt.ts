import { prioritizeEquipmentText } from '../lib/pprTextPrioritize'

export const PPR_TOOLS_EXTRACTION_SYSTEM_PROMPT = `Ты — эксперт по промышленной безопасности ТОО «Урал Ойл энд Газ» (UOG).

Проанализируй документ ППР и составь структурированный список всего оборудования, инструментов и материалов, упомянутых в тексте.

Ответ — ТОЛЬКО валидный JSON без markdown-обёртки:
{
  "categories": [
    {
      "title": "Основное технологическое оборудование",
      "items": [{ "name": "Очистное устройство (пиг)" }]
    },
    {
      "title": "Трубопроводная арматура",
      "items": [{ "name": "U12GC16", "tag": "U12GC16" }]
    },
    {
      "title": "Вспомогательные приспособления и расходные материалы",
      "items": [{ "name": "Торцевая заглушка 6\\"" }]
    },
    {
      "title": "Средства контроля и измерения",
      "items": [{ "name": "Манометры" }]
    },
    {
      "title": "Средства индивидуальной защиты и безопасности",
      "items": [{ "name": "Комплект ЛАРН (Spill Kit)" }]
    }
  ]
}

Правила:
- Раздели список ровно по 5 категориям из примера (пустые категории — items: []).
- Для каждой позиции — только поле name (краткое наименование или тег/обозначение арматуры).
- Для арматуры в name указывай тег: U12GC16, 100GC51 и т.д.
- Без описаний, назначений, нумерации строк таблицы, без этапов работ.
- Не выдумывай позиции, которых нет в документе.
- Без дубликатов.`

export function buildToolsExtractionUserPrompt(docText: string, fileName: string): string {
  const trimmed = prioritizeEquipmentText(docText, 28000)
  return `Файл: ${fileName}

Проанализируй документ и составь структурированный список всего оборудования, инструментов и материалов.

Раздели список по категориям:
1. Основное технологическое оборудование
2. Трубопроводная арматура (с указанием тегов/обозначений)
3. Вспомогательные приспособления и расходные материалы
4. Средства контроля и измерения
5. Средства индивидуальной защиты и безопасности

Для каждой позиции укажи только наименование (поле name в JSON).

Текст документа ППР:
---
${trimmed}
---`
}
