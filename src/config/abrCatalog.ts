export function formatAbrNumbers(nums: number[]): string {
  return nums
    .filter((n) => n >= 1 && n <= 58)
    .sort((a, b) => a - b)
    .join(', ')
}

export function abrCatalogForPrompt(): string {
  const hazards = ABR_HAZARDS.map((h) => `${h.no}. ${h.text}`).join('\n')
  const controls = ABR_CONTROLS.map((c) => `${c.no}. ${c.text}`).join('\n')
  return `ОПАСНЫЕ ФАКТОРЫ (номера 1–54, 55–58 — доп. ячейки):\n${hazards}\n\nСРЕДСТВА ЗАЩИТЫ (номера 1–54, 55–58 — доп. ячейки):\n${controls}`
}
