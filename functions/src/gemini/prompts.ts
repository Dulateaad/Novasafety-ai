/** Промпт для извлечения мер контроля из текста Method Statement / ППР. */
export const PPR_CONTROL_MEASURES_SYSTEM_PROMPT = `Ты — эксперт по промышленной безопасности ТОО «Урал Ойл энд Газ» (UOG).
По тексту программы производства работ (Method Statement, ПОР, ППР) извлеки данные для наряда-допуска и меры контроля для формы АСОР.

Ответ — ТОЛЬКО валидный JSON без markdown-обёртки:
{
  "workTitle": "наименование работ (суть, без префикса ППР)",
  "workTasks": [
    {
      "taskTitle": "название этапа без номера 3.1",
      "workContent": "полное описание этапа: операции, объём, охват, последовательность, оборудование на этапе"
    }
  ],
  "toolsAndEquipment": "инструменты и оборудование из ППР",
  "items": [
    {
      "section": "раздел документа",
      "hazard": "опасный фактор",
      "controlMeasures": ["мера 1", "мера 2"]
    }
  ]
}

Правила workTitle:
- Только суть работ из шапки (после «РАБОТ ПО»), без префикса «ПЛАН ОРГАНИЗАЦИИ».
- Обычный регистр, не КАПС.

Правила workTasks (главный блок):
- Каждый этап из разделов 2–3 ППР: taskTitle (без 3.1) + workContent (описание, объём, операции).
- НЕ разделяй на отдельные «описание» и «объём» — всё в workTasks по этапам.

Правила items:
- Меры из «Техника безопасности», «Опасные зоны», подразделов 3.x.
- Минимум 5 items при достаточном материале.
- Язык: русский.`

export function buildControlMeasuresUserPrompt(docText: string, fileName: string): string {
  const trimmed = docText.length > 28000 ? docText.slice(0, 28000) + '\n…[текст обрезан]' : docText
  return `Файл: ${fileName}

Текст документа ППР:
---
${trimmed}
---

Извлеки workTasks (этапы с описаниями), toolsAndEquipment и items в JSON по схеме из системного промпта.`
}
